    private void tableCategoryMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableCategoryMouseClicked
    	DefaultTableModel model = (DefaultTableModel) tableCategory.getModel();
    	int index= tableCategory.getSelectedRow();
    	categoryId=Integer.parseInt(model.getValueAt(index, 0).toString());
    	String categoryName=model.getValueAt(index, 1).toString();
        txtName.setText(categoryName);
        btnSave.setEnabled(false);
        btnUpdate.setEnabled(true);
    }//GEN-LAST:event_tableCategoryMouseClicked

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
    	try {
    		
    		String categoryName=txtName.getText();
    		if (StringUtils.isNotBlank(categoryName)) {
    			Category category= new Category(null, categoryName, new Date(), "admin", null, null);
            	inventoryManagementUtility.save(category);
            	JOptionPane.showMessageDialog(null, "Category saved successfully!");
            	populateCategoryTable();
			}else {
				JOptionPane.showMessageDialog(null, "Please provide all fields!");
			}
		} catch (Exception e) {
			JOptionPane.showMessageDialog(null, e);
		}
    	clearAllFields();
    }//GEN-LAST:event_btnSaveActionPerformed

