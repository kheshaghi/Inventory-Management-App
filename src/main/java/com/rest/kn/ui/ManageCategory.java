    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
    	try {
    		DefaultTableModel model = (DefaultTableModel) tableCategory.getModel();
        	int index= tableCategory.getSelectedRow();
        	String categoryNameInTable=model.getValueAt(index, 1).toString();
    		
    		String categoryName=txtName.getText();
    		if (StringUtils.isNotBlank(categoryName)) {
    			if (categoryNameInTable.equals(categoryName)) {
    				JOptionPane.showMessageDialog(null, "Please provide new category name");
				}else {
					Optional<Category> categoryOptional= inventoryManagementUtility.getCategoryDetails(categoryId);
	    			if (categoryOptional.isPresent()) {
	    				Category category=categoryOptional.get();
	    				category.setCategoryName(categoryName);
	    				inventoryManagementUtility.save(category);
	                	JOptionPane.showMessageDialog(null, "Category Updated successfully!");
	                	populateCategoryTable();
	    			}
				}
			}else {
				JOptionPane.showMessageDialog(null, "Please provide all fields!");
			}
			
		} catch (Exception e) {
			JOptionPane.showMessageDialog(null, e);
		}
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void clearAllFields() {
		// TODO Auto-generated method stub
		txtName.setText("");
		btnSave.setEnabled(true);
		btnUpdate.setEnabled(false);
	}

	private void populateCategoryTable() {
		DefaultTableModel model = (DefaultTableModel) tableCategory.getModel();
		model.setRowCount(0);
		try {
			List<Category> categoryList	=inventoryManagementUtility.preloadCategoryTable();
			
			for (Category category : categoryList) {
				model.addRow(new Object[] {category.getId(),category.getCategoryName()});
			}
		} catch (Exception e) {
				JOptionPane.showMessageDialog(null, e);
		}
		model.fireTableDataChanged();
		clearAllFields();
	}

